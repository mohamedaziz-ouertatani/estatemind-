export type GovernorateType = keyof typeof TUNISIA_LOCATIONS

export function getDelegations(governorate: string): string[] {
  if (!governorate || !(governorate in TUNISIA_LOCATIONS)) return []
  return Object.keys(TUNISIA_LOCATIONS[governorate as GovernorateType].delegations)
}

export function getNeighborhoods(governorate: string, delegation: string): string[] {
  if (!governorate || !delegation || !(governorate in TUNISIA_LOCATIONS)) return []
  const gov = TUNISIA_LOCATIONS[governorate as GovernorateType]
  return gov.delegations[delegation as keyof typeof gov.delegations] || []
}
